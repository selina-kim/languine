        review_logs = []
        base_date = datetime(2025, 1, 1, 0, 0, 0, 0, timezone.utc)

        # Card 1: day 0, day 1, day 2 → first review doesn't count, days 1 and 2 each count → 2
        card1 = Card()
        card1, log = scheduler.review_card(
            card=card1, grade=Grade.Good, review_datetime=base_date
        )
        review_logs.append(log)
        card1, log = scheduler.review_card(
            card=card1, grade=Grade.Good, review_datetime=base_date + timedelta(days=1)
        )
        review_logs.append(log)
        card1, log = scheduler.review_card(
            card=card1, grade=Grade.Good, review_datetime=base_date + timedelta(days=2)
        )
        review_logs.append(log)

        # Card 2: both reviews on day 0 (same day) → 0 count
        card2 = Card()
        card2, log = scheduler.review_card(
            card=card2, grade=Grade.Good, review_datetime=base_date
        )
        review_logs.append(log)
        card2, log = scheduler.review_card(
            card=card2, grade=Grade.Good, review_datetime=base_date + timedelta(minutes=10)
        )
        review_logs.append(log)

        # Card 3: single review only → 0 count
        card3 = Card()
        card3, log = scheduler.review_card(
            card=card3, grade=Grade.Good, review_datetime=base_date
        )
        review_logs.append(log)

        optimizer = Optimizer(review_logs=review_logs)

        assert optimizer._num_reviews() == 2

