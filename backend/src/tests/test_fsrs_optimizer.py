def get_revlogs() -> list[ReviewLog]:
    """
    reads a csv of prepared exported anki review logs
    and returns them as a list of ReviewLog objects
    """

    df = pd.read_csv("src/tests/sample_data/sample_logs.csv")

    review_logs = []
    for index, row in df.iterrows():
        card_id = row["card_id"]
        grade = Grade(row["grade"])
        review_datetime = datetime.fromisoformat(row["review_datetime"])
        review_duration = row["review_duration"]

        review_log = ReviewLog(
            card_id=str(card_id),
            grade=grade,
            review_datetime=review_datetime,
            review_duration=review_duration,
        )

        review_logs.append(review_log)

    return review_logs


def get_large_revlogs() -> list[ReviewLog]:
    """
    Reads a larger synthetic CSV of review logs (5,000 cards, ~89k reviews)
    generated by src/tests/sample_data/generate_large_sample_logs.py.

    Used for split-based tests where the 10% val/test slices need enough reviews
    to produce statistically stable BCELoss values.
    """
    df = pd.read_csv("src/tests/sample_data/large_sample_logs.csv")

    review_logs = []
    for _, row in df.iterrows():
        review_log = ReviewLog(
            card_id=str(row["card_id"]),
            grade=Grade(row["grade"]),
            review_datetime=datetime.fromisoformat(row["review_datetime"]),
            review_duration=row["review_duration"],
        )
        review_logs.append(review_log)

    return review_logs

def get_min_logs_for_optimization(review_logs):
    for i in range(1, len(review_logs) + 1):
        subset = review_logs[:i]
        optimizer = Optimizer(review_logs=subset)
        if optimizer._num_reviews() >= 512:
            return subset
    return None

def collect_predictions(
        parameters: list[float], review_logs: list[ReviewLog]
    ) -> tuple[list[float], list[int]]:
        """
        Helper: replay each card's review history with the given parameters and
        collect (y_pred, y_true) pairs for all delayed (non-same-day) reviews.
        """
        scheduler = Scheduler(parameters=parameters)
        optimizer_tmp = Optimizer(review_logs=review_logs)
        revlogs_dict = optimizer_tmp._revlogs_train

        y_preds: list[float] = []
        y_trues: list[int] = []

        for card_id, card_review_history in revlogs_dict.items():
            for i, review in enumerate(card_review_history[:max_seq_len]):
                x_date = review[0][0]
                u_grade = review[0][1]
                y_true = review[1]

                if i == 0:
                    card = Card(card_id=card_id, due=x_date)

                if card.last_review and (x_date - card.last_review).days > 0:
                    y_pred = scheduler.get_card_retrievability(
                        card=card, current_datetime=x_date
                    )
                    if isinstance(y_pred, torch.Tensor):
                        y_pred = y_pred.item()
                    y_preds.append(float(y_pred))
                    y_trues.append(int(y_true))

                card, _ = scheduler.review_card(
                    card=card, grade=u_grade, review_datetime=x_date, review_duration=None
                )

        return y_preds, y_trues

class TestOptimizer:
    def test_zero_revlogs(self):
        """
        if no review logs are provided, the optimal parameters should not change
        from the starting parameters
        """

        review_logs = []

        optimizer = Optimizer(review_logs=review_logs)

        optimal_parameters = optimizer.compute_optimal_parameters()

        assert optimal_parameters == list(DEFAULT_PARAMETERS)

    def test_review_logs(self):
        """
        test the optimizer on a set of exported anki review logs
        """

        review_logs = get_revlogs()

        optimizer = Optimizer(review_logs=review_logs)

        optimal_parameters = optimizer.compute_optimal_parameters()

        # the optimal paramaters are no longer equal to the starting parameters
        assert optimal_parameters != list(DEFAULT_PARAMETERS)

        # the output is expected and deterministic
        assert np.allclose(optimal_parameters, test_optimal_parameters)

        # the computed loss with the optimized parameters are less than that of the starting parameters
        starting_loss = optimizer._compute_batch_loss(parameters=DEFAULT_PARAMETERS)
        optimized_loss = optimizer._compute_batch_loss(parameters=optimal_parameters)
        assert optimized_loss < starting_loss

        # calling the same optimizer again will yield the same parameters
        optimal_parameters_again = optimizer.compute_optimal_parameters()
        assert optimal_parameters == optimal_parameters_again

        # initializing another optimizer will give the same results as the original optimizer
        optimizer_new = Optimizer(review_logs=review_logs)
        optimal_parameters_new = optimizer_new.compute_optimal_parameters()
        assert optimal_parameters == optimal_parameters_new

